			<li>
				<span>どんな歯並びでもインビザラインで治りますか？</span>
				重度の叢生（ガタガタの歯並び）や重度の不正咬合、極度の上下顎前突の場合には歯を抜いてワイヤー矯正を行う方が適している場合があります。インビザライン治療が適応するかどうかは歯並びやかみ合わせ、顎や口元の状態を診させて頂いてからの診断となります。
				また、重度の症例でない場合でも、インビザラインと部分的な矯正装置やワイヤーを併用して治療を行うこともあります。</li>
			<li>
				<span>アライナーは痛くないですか？</span>
				装着直後は硬く締め付けられるような違和感が生じることがあります。数分で装着時の違和感はなくなります。アライナー装着中に痛みが継続することはないのでご安心ください。</li>
			<li>
				<span>インビザラインでちゃんと歯は動きますか？</span>
				アライナーを正しく装着し、決められた装着時間を守っていれば必ず歯は動きます。また、アライナーだけでなくゴムを使用する場合も、指示通りに行なって頂くことが重要です。１日に２０時間の装着をお願いしておりますが
				これより装着時間が少ないと、治療計画通りに歯が動かず治療期間の延長につながりますので注意してください。</li>
			<li>
				<span>妊娠しても治療は続けられますか？</span>
