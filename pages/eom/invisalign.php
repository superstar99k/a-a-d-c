			<li>
				<span>長期で海外に行く予定や引越しなどの場合にはどうなりますか？</span>
				長期で海外に行かれる場合や、引越しで通院が難しくなった場合には、国内外のインビザラインドクターのいるクリニックに転院することができます。転院先のドクターの判断にもよりますが、今までの治療計画に沿ったアライナーでの継続も可能です。</li>
			<li>
				<span>親知らずがあるのですが、抜く必要がありますか？</span>
