			<li>
				<span>•	ブリッジやインプラントがあってもインビザライン治療はできますか？</span>
				ブリッジやインプラントがある場合、その歯だけを全く動かさないようなプランにすることができます。矯正のために外したり再治療の必要はありません。</li>
			<li>
				<span>ブリッジがあってもインビザライン治療はできますか？</span>
				ブリッジがある場合のインビザライン治療では、事前にブリッジを切断して、１本１本の歯を単独にしておく必要があります。治療中は歯のない部分にアライナーにダミーの歯をつけることができるので、審美的に治療を行うことができます。矯正治療後は補綴処置を行い噛み合わせを治します。</li>
			<li>
				<span>治療中に虫歯になったらどうすれば良いですか？</span>
				治療中でも虫歯の治療は可能です。小さい虫歯なら、即日で治して装着しているアライナーで治療を継続することができます。大きめの虫歯や詰め物や被せ物の脱離など、歯の形が大きく変わるような場合には、虫歯治療後に再度型取りをし直してインビザライン治療を再スタートすることになります。</li>
			<li>
				<span>インビザラインのメリット、デメリットはなんですか？</span>
				メリットは、歯にワイヤーをつけなくて良いことです。ほとんど痛みもなく目立ちません。また、取り外しができるため食事やブラッシングが普段通り行うことができます。ぶつかっても口の中が傷つくことがないのでスポーツや楽器などの趣味がある方でも安心して矯正治療が行えます。デメリットは、正しく装着し、装着時間を守らないと治療計画通りに歯が動かず、治療期間が延びてしまったり、後戻りして治療が進まなくなることです。また、マウスピースを装着することが苦手な方には向いていません。歯並びの状態によってはワイヤー矯正よりも治療期間が長くなることもあります。</li>
			<li>
				<span>ワイヤー矯正とインビザラインではどちらが早く終わりますか？</span>
				歯並びの状態や歯の動きには個人差も大きく、一概にどちらが早いとも断言できないのですが、非抜歯全顎矯正で行った場合にはワイヤー矯正の方が早く終わることが多いです。部分的な範囲で行なう矯正では、数か月単位での大きな違いはありません。</li>
			<li>
				<span>インビザライン中でもホワイトニングはできますか？</span>
				ホワイトニングは行うことはできます。ただし、矯正治療中は歯を動かしているため知覚過敏が起きやすく、刺激が加わることでしみることがあります。
				また、ホワイトニング後しばらくは色素を取り込みやすくなるため、インビザラインのようなマウスピース矯正の場合、歯に着色が停滞しやすくなることもあります。急な必要性がない場合には、インビザライン治療中のホワイトニングはあまりお勧めではありません。ホワイトニングをする場合には、知覚過敏や着色汚れに対してのケアをしっかり行うことが必要になります。</li>
			<li>
				<span>インビザライン治療でも保定は必要ですか？</span>
				はい、必要です。インビザラインなどのマウスピース矯正、ワイヤー矯正、裏側矯正など、どの矯正方法で行った場合でも矯正治療後の保定は後戻り防止のために必要です。インビザラインでは、専用のビベラリテーナーを治療終了後に使用して頂いております。保定期間は通常、終了後１年は夜間日中通して装着して頂き、その後は夜間のみ、または１日〇〇時間など、時間を短くして装着することをお願いしています。</li>
		</ul>
